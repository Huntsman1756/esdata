def seed():
    conn = psycopg.connect(DB_URL)
    cur = conn.cursor()

    for c in CUENTAS:
        codigo, desc, nivel, nivel_int, padre, grupo, clase, saldo_normal, tipo_cuenta = c
        cur.execute(
            """
            INSERT INTO pgc_cuenta (id, codigo, descripcion, nivel, padre_codigo, grupo, clase, saldo_normal, tipo_cuenta)
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
            ON CONFLICT (codigo) DO UPDATE SET
                descripcion = EXCLUDED.descripcion,
                nivel = EXCLUDED.nivel,
                padre_codigo = EXCLUDED.padre_codigo,
                grupo = EXCLUDED.grupo,
                clase = EXCLUDED.clase,
                saldo_normal = EXCLUDED.saldo_normal,
                tipo_cuenta = EXCLUDED.tipo_cuenta
            """,
            (str(uuid.uuid5(uuid.NAMESPACE_DNS, codigo)), codigo, desc, nivel_int, padre, grupo, clase, saldo_normal, tipo_cuenta),
        )

    conn.commit()
    print(f"Seeded {len(CUENTAS)} pgc_cuenta records")
    conn.close()


